CK_ULONG
ber_encode_INTEGER(CK_BBOOL    length_only,
	CK_BYTE  ** ber_int,
	CK_ULONG  * ber_int_len,
	CK_BYTE   * data,
	CK_ULONG    data_len)
{
	CK_BYTE   *buf = NULL;
	CK_ULONG   len;

	/*
	 * if data_len < 127 use short form length id
	 * if data_len < 256 use long form length id with
	 * byte length field
	 * if data_len < 65536 use long form length id with
	 * byte length field
	 * if data_len < 16777216 use long form length id
	 * with byte length field
	 */
	if (data_len < 128)
		len = 1 + 1 + data_len;
	else if (data_len < 256)
		len = 1 + (1 + 1) + data_len;
	else if (data_len < (1 << 16))
		len = 1 + (1 + 2) + data_len;
	else if (data_len < (1 << 24))
		len = 1 + (1 + 3) + data_len;
	else
		return (CKR_FUNCTION_FAILED);

	if (length_only == TRUE) {
		*ber_int_len = len;
		return (CKR_OK);
	}

	buf = (CK_BYTE *)malloc(len);
	if (! buf) {
		return (CKR_HOST_MEMORY);
	}
	if (data_len < 128) {
		buf[0] = 0x02;
		buf[1] = data_len;
		(void) memcpy(&buf[2], data, data_len);

		*ber_int_len = len;
		*ber_int = buf;
		return (CKR_OK);
	}

	if (data_len < 256) {
		buf[0] = 0x02;
		buf[1] = 0x81;
		buf[2] = data_len;
		(void) memcpy(&buf[3], data, data_len);

		*ber_int_len = len;
		*ber_int = buf;
		return (CKR_OK);
	}

	if (data_len < (1 << 16)) {
		buf[0] = 0x02;
		buf[1] = 0x82;
		buf[2] = (data_len >> 8) & 0xFF;
		buf[3] = (data_len) & 0xFF;
		(void) memcpy(&buf[4], data, data_len);

		*ber_int_len = len;
		*ber_int = buf;
		return (CKR_OK);
	}

	if (data_len < (1 << 24)) {
		buf[0] = 0x02;
		buf[1] = 0x83;
		buf[2] = (data_len >> 16) & 0xFF;
		buf[3] = (data_len >>  8) & 0xFF;
		buf[4] = (data_len) & 0xFF;
		(void) memcpy(&buf[5], data, data_len);

		*ber_int_len = len;
		*ber_int = buf;
		return (CKR_OK);
	}

	free(buf);
	return (CKR_FUNCTION_FAILED);
}

CK_RV
ber_decode_INTEGER(CK_BYTE   * ber_int,
	CK_BYTE  ** data,
	CK_ULONG  * data_len,
	CK_ULONG  * field_len)
{
	CK_ULONG  len, length_octets;

	if (! ber_int) {
		return (CKR_FUNCTION_FAILED);
	}
	if (ber_int[0] != 0x02) {
		return (CKR_FUNCTION_FAILED);
	}
	if ((ber_int[1] & 0x80) == 0) {
		len = ber_int[1] & 0x7F;

		*data = &ber_int[2];
		*data_len  = len;
		*field_len = 1 + 1 + len;
		return (CKR_OK);
	}

	length_octets = ber_int[1] & 0x7F;

	if (length_octets == 1) {
		len = ber_int[2];

		*data = &ber_int[3];
		*data_len  = len;
		*field_len = 1 + (1 + 1) + len;
		return (CKR_OK);
	}

	if (length_octets == 2) {
		len = ber_int[2];
		len = len << 8;
		len |= ber_int[3];

		*data = &ber_int[4];
		*data_len  = len;
		*field_len = 1 + (1 + 2) + len;
		return (CKR_OK);
	}

	if (length_octets == 3) {
		len = ber_int[2];
		len = len << 8;
		len |= ber_int[3];
		len = len << 8;
		len |= ber_int[4];

		*data = &ber_int[5];
		*data_len  = len;
		*field_len = 1 + (1 + 3) + len;
		return (CKR_OK);
	}

	return (CKR_FUNCTION_FAILED);
}

CK_RV
ber_encode_OCTET_STRING(CK_BBOOL    length_only,
CK_BYTE  ** str,
CK_ULONG  * str_len,
CK_BYTE   * data,
CK_ULONG    data_len)
{
	CK_BYTE   *buf = NULL;
	CK_ULONG   len;


	/*
	 * if data_len < 128 use shor - form length id
	 * if data_len < 256 use lon - form length id with - byte length field
	 * if data_len < 65536 use lon - form length id with - byte length field
	 */
	if (data_len < 128)
		len = 1 + 1 + data_len;
	else if (data_len < 256)
		len = 1 + (1 + 1) + data_len;
	else if (data_len < (1 << 16))
		len = 1 + (1 + 2) + data_len;
	else if (data_len < (1 << 24))
		len = 1 + (1 + 3) + data_len;
	else
		return (CKR_FUNCTION_FAILED);

	if (length_only == TRUE) {
		*str_len = len;
		return (CKR_OK);
	}

	buf = (CK_BYTE *)malloc(len);
	if (! buf) {
		return (CKR_HOST_MEMORY);
	}

	if (data_len < 128) {
		buf[0] = 0x04; /* primitive, OCTET STRING */
		buf[1] = data_len;
		(void) memcpy(&buf[2], data, data_len);

		*str_len = len;
		*str = buf;
		return (CKR_OK);
	}

	if (data_len < 256) {
		buf[0] = 0x04;	/* primitive, OCTET STRING */
		buf[1] = 0x81;	/* length header -- 1 length octets */
		buf[2] = data_len;

		(void) memcpy(&buf[3], data, data_len);

		*str_len = len;
		*str = buf;
		return (CKR_OK);
	}

	if (data_len < (1 << 16)) {
		buf[0] = 0x04;	/* primitive, OCTET STRING */
		buf[1] = 0x82;	/* length header -- 2 length octets */
		buf[2] = (data_len >> 8) & 0xFF;
		buf[3] = (data_len) & 0xFF;

		(void) memcpy(&buf[4], data, data_len);

		*str_len = len;
		*str = buf;
		return (CKR_OK);
	}

	if (data_len < (1 << 24)) {
		buf[0] = 0x04;	/* primitive, OCTET STRING */
		buf[1] = 0x83;	/* length header -- 3 length octets */
		buf[2] = (data_len >> 16) & 0xFF;
		buf[3] = (data_len >>  8) & 0xFF;
		buf[4] = (data_len) & 0xFF;

		(void) memcpy(&buf[5], data, data_len);

		*str_len = len;
		*str = buf;
		return (CKR_OK);
	}

	free(buf);
	return (CKR_FUNCTION_FAILED);
}

CK_RV
ber_decode_OCTET_STRING(CK_BYTE  * str,
	CK_BYTE ** data,
	CK_ULONG * data_len,
	CK_ULONG * field_len)
{
	CK_ULONG  len, length_octets;

	if (! str) {
		return (CKR_FUNCTION_FAILED);
	}
	if (str[0] != 0x04) {
		return (CKR_FUNCTION_FAILED);
	}
	if ((str[1] & 0x80) == 0) {
		len = str[1] & 0x7F;

		*data = &str[2];
		*data_len  = len;
		*field_len = 1 + (1) + len;
		return (CKR_OK);
	}

	length_octets = str[1] & 0x7F;

	if (length_octets == 1) {
		len = str[2];

		*data = &str[3];
		*data_len  = len;
		*field_len = 1 + (1 + 1) + len;
		return (CKR_OK);
	}

	if (length_octets == 2) {
		len = str[2];
		len = len << 8;
		len |= str[3];

		*data = &str[4];
		*data_len  = len;
		*field_len = 1 + (1 + 2) + len;
		return (CKR_OK);
	}

	if (length_octets == 3) {
		len = str[2];
		len = len << 8;
		len |= str[3];
		len = len << 8;
		len |= str[4];

		*data = &str[5];
		*data_len  = len;
		*field_len = 1 + (1 + 3) + len;
		return (CKR_OK);
	}

	/* > 3 length octets implies a length > 16MB */
	return (CKR_FUNCTION_FAILED);
}

