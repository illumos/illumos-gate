void stlogit(char *, ...);


extern int  debugfile;
pthread_mutex_t  lmtx = PTHREAD_MUTEX_INITIALIZER;

static int enabled = 0;
static int logging = 0;
static int env_log_check = 0;

void
stloginit() {
	char *logval;
	if (! env_log_check) {
		logval = getenv("PKCS_ERROR_LOG");
		env_log_check = 1;
		if (logval != NULL)
			logging = 1;
		else
		logging = 0;
	}
	if (! enabled && logging) {
		enabled = 1;
		openlog((const char *)token_specific.token_debug_tag,
		    LOG_PID | LOG_NDELAY, LOG_LOCAL6);
		(void) setlogmask(LOG_UPTO(LOG_DEBUG));

#ifdef DEBUG
		debugfile = 1;
#else
		debugfile = 0;
#endif
		stlogit("Log initialized");
	}
}

void
stlogterm()
{
	enabled = 0;
}

/*PRINTFLIKE2*/
void
stlogit2(int type, char *fmt, ...)
{
	va_list pvar;
	char buffer[4096*4];

	if (! enabled)  stloginit();

	if (enabled && type) {
		va_start(pvar, fmt);
		(void) vsnprintf(buffer, sizeof (buffer), fmt, pvar);
		va_end(pvar);
		(void) pthread_mutex_lock(&lmtx);
		syslog(LOG_DEBUG, buffer);
		(void) pthread_mutex_unlock(&lmtx);
	}
}

/*PRINTFLIKE1*/
void
stlogit(char *fmt, ...)
{
	va_list pvar;
	char buffer[4096*4];

	if (! enabled)  stloginit();

	if (enabled && debugfile) {
		va_start(pvar, fmt);
		(void) vsnprintf(buffer, sizeof (buffer), fmt, pvar);
		va_end(pvar);
		(void) pthread_mutex_lock(&lmtx);
		syslog(LOG_DEBUG, buffer);
		(void) pthread_mutex_unlock(&lmtx);
	}
}

