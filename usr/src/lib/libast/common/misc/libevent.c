#include <error.h>

int
libevent(void* handle, void* discipline, int level, ...)
{
	va_list	ap;

	va_start(ap, level);
	errorv((level & ERROR_LIBRARY) ? *((char**)handle) : (char*)0, level, ap);
	va_end(ap);
	return 0;
}
