static int
block(fp, gz, buf)
FILE*	fp;
gzFile	gz;
char*	buf;
{
	int	r;

	if (gz)
		r = gzread(gz, buf, sizeof(Header_t)) == sizeof(Header_t);
	else
		r = fread(buf, sizeof(Header_t), 1, fp) == 1;
	if (r)
		state.blocks++;
	return r;
}

static int
skip(fp, gz, buf, n)
FILE*		fp;
gzFile		gz;
char*		buf;
unsigned long	n;
{
	while (n > 0)
	{
		if (!block(fp, gz, buf))
		{
			fprintf(stderr, "%s: unexpected EOF\n", state.id);
			return 1;
		}
		if (n <= sizeof(Header_t))
			break;
		n -= sizeof(Header_t);
	}
	return 0;
}

static unsigned long
number(s)
register char*	s;
{
	unsigned long	n = 0;

	while (*s == ' ')
		s++;
	while (*s >= '0' && *s <= '7')
		n = (n << 3) + (*s++ - '0');
	return n;
}

